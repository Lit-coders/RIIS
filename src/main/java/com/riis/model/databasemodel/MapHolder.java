package com.riis.model.databasemodel;

public class MapHolder {
    private int mapHolderID;
    private String mapHolderName;
    private String mapHolderFName;
    private String mapHolderGFName;
    private String mapHolderPhoneNum;
    private byte[] mapHolderPhoto;

    public MapHolder() {
    }

    public int getMapHolderID() {
        return mapHolderID;
    }

    public void setMapHolderID(int mapHolderID) {
        this.mapHolderID = mapHolderID;
    }

    public String getMapHolderName() {
        return mapHolderName;
    }

    public void setMapHolderName(String mapHolderName) {
        this.mapHolderName = mapHolderName;
    }

    public String getMapHolderFName() {
        return mapHolderFName;
    }

    public void setMapHolderFName(String mapHolderFName) {
        this.mapHolderFName = mapHolderFName;
    }

    public String getMapHolderGFName() {
        return mapHolderGFName;
    }

    public void setMapHolderGFName(String mapHolderGFName) {
        this.mapHolderGFName = mapHolderGFName;
    }

    public String getMapHolderPhoneNum() {
        return mapHolderPhoneNum;
    }

    public void setMapHolderPhoneNum(String mapHolderPhoneNum) {
        this.mapHolderPhoneNum = mapHolderPhoneNum;
    }

    public byte[] getMapHolderPhoto() {
        return mapHolderPhoto;
    }

    public void setMapHolderPhoto(byte[] mapHolderPhoto) {
        this.mapHolderPhoto = mapHolderPhoto;
    }
}
